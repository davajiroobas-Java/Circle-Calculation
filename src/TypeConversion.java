public class TypeConversion {

    public static void main(String[]args){


        int myInt=6;
        double myDouble=8.88;

        //Convert int to double
        double intToDouble= myInt;

        //Convert double to int
        int doubleToInt=(int)myDouble;

        //Print result
        System.out.println("\n--- Type Conversion Examples ---");

        System.out.println("\nOrigin int value " + myInt);
        System.out.println("Convert to double " + intToDouble);



        System.out.println("\nOrigin double value " + myDouble);
        System.out.println("Convert to int " + doubleToInt);



    }

}
